package com.teleclub.rajatalk.model;

public class Account {
    private String id;
    private String phone;
    private String i_account;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getIAccount() {
        return i_account;
    }

    public void setIAccount(String i_account) {
        this.i_account = i_account;
    }
}
